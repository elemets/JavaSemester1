public class YearsBeforeRetirement
{
  public static void main(String[] args)
  {
    int MyAge = 19;
    int RetireAge = 69;
    int YearsLeftWorking =  RetireAge - MyAge;
    System.out.println("My age is now " + MyAge);
    System.out.println("I will retire at age " + RetireAge);
    System.out.println("Years left working is " + YearsLeftWorking);
  }
}
